
/**
 * Representa la información simple de un usuario
 */export interface User {
    /**
     * Nombre del usuario
     */
    name?: string;
    /**
     * Curp de usuario
     */
    curp?: string;
    /**
     * Folio de seguro popular
     */
    folio?: string;
    /**
     * Edad del usuario
     */
    age?: number;
    /**
     * Género del usuario
     */
    gender?: string;
    /**
     * Fecha de nacimiento
     */
    dateOfBirth?: Date;


}
